function createWorkArticle(work) {
  const workArticle = document.createElement('article');
  workArticle.classList.add('project');
  workArticle.innerHTML += `
  <div class="projectPicture">
    <img />
  </div>
  <div class="projectInfo">
    <h3 class="projectTitle">Multi-Post Stories <br />Gain+Glory</h3>
    <ul class="greyProjectButtons">
      <li class="projectListItems">${work.technologies[0]}</li>
      <li class="projectListItems">${work.technologies[1]}</li>
      <li class="projectListItems">${work.technologies[2]}</li>
      <li class="projectListItems">${work.technologies[3]}</li>
    </ul>
    <div class="greenProjectButton">
      <button class="LinkButton" id="${work.identification}" >See project</button>
    </div>
  </div>`;
  workDiv.appendChild(workArticle);
}

